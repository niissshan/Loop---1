
#include<iostream>
using namespace std;
int main(){
    cout<<"The Upper case alphabets are "<<endl;
    int i=65;
    while(i<=90)
    {
     cout<<(char)i<<" "<<i<<endl;
     i++;
    }
    cout<<"The lower case alphabets are "<<endl;
    int j=97;
    while(j<=122)
    {
     cout<<(char)j<<" "<<j<<endl;
     j++;
    }
    
}